class Solution{
    public:
    // Function to find element in sorted array
    // arr: input array
    // N: size of array
    // K: element to be searche
    int searchInSorted(int arr[], int N, int K) 
    { 
        
       // Your code here
       int l,r;
       l=0;
       r =N-1;
       while(l<=r){
        int m =(l+r)/2;
        if(K==arr[m])
        return 1;
        else if(K<arr[m])
        r=m-1;
        else
        l=m+1;
       }return -1;
       
    }
};

